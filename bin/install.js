const CLAUDE_DIR = path.join(os.homedir(), '.claude');
const STATUSLINE_PATH = path.join(CLAUDE_DIR, 'statusline-command.sh');
const SETTINGS_PATH = path.join(CLAUDE_DIR, 'settings.json');

function generateStatuslineScript(fields) {
  const has = (i) => fields.includes(i);
  // 0=Model 1=Progress 2=Cache 3=Git 4=Duration 5=Cost 6=CWD
  const hasLine2 = has(4) || has(5) || has(6);

  let script = `#!/bin/bash
# Claude Code statusline — generated by yozakura
# Line 1: Model │ ████░░░░░░ 27% (54k/200k) │ cache │ ⎇ git
# Line 2: ⏱ duration │ $cost │ ~/cwd

input=$(cat)

# ── Extract all fields in one jq call ──
eval "$(echo "$input" | jq -r '
  def num: if . == null then 0 elif type == "number" then . else (tostring | gsub("[^0-9.]"; "") | if . == "" then 0 else tonumber end) end;
  def safe_str: if . == null then "" else tostring end;

  "MODEL=" + (.model | safe_str | @sh),
  "CWD=" + ((.workspace.current_dir // .cwd // "") | safe_str | @sh),
  "PCT_RAW=" + ((.context_window.used_percentage // 0) | num | tostring | @sh),
  "CTX_SIZE=" + ((.context_window.context_window_size // 200000) | num | tostring | @sh),
  "INPUT_TOKENS=" + ((if (.context_window.current_usage | type) == "object" then .context_window.current_usage.input_tokens // 0 else 0 end) | num | tostring | @sh),
  "CACHE_CREATE=" + ((if (.context_window.current_usage | type) == "object" then .context_window.current_usage.cache_creation_input_tokens // 0 else 0 end) | num | tostring | @sh),
  "CACHE_READ=" + ((if (.context_window.current_usage | type) == "object" then .context_window.current_usage.cache_read_input_tokens // 0 else 0 end) | num | tostring | @sh),
  "USAGE_NUMERIC=" + ((if (.context_window.current_usage | type) == "number" then .context_window.current_usage else .context_window.total_input_tokens // 0 end) | num | tostring | @sh),
  "COST_USD=" + ((.cost.total_cost_usd // 0) | num | tostring | @sh),
  "DURATION_MS=" + ((.cost.total_duration_ms // 0) | num | tostring | @sh)
')"

# ── Colors ──
RST='\\033[0m'
DIM='\\033[2m'
CYAN='\\033[1;36m'
MAGENTA='\\033[1;35m'
GREEN='\\033[32m'
YELLOW='\\033[33m'
RED='\\033[31m'

SEP="\${DIM} │ \${RST}"`;

  // ── Model (field 0) ──
  if (has(0)) {
    script += `

# ── Model: claude-opus-4-6-20250313 → Opus 4.6 ──
DISPLAY_MODEL="$MODEL"
if [[ "$MODEL" =~ claude-([a-z]+)-([0-9]+)-([0-9]+) ]]; then
    NAME="\${BASH_REMATCH[1]}"
    DISPLAY_MODEL="$(tr '[:lower:]' '[:upper:]' <<< "\${NAME:0:1}")\${NAME:1} \${BASH_REMATCH[2]}.\${BASH_REMATCH[3]}"
fi`;
  }

  // ── Progress bar (field 1, always generated as core) ──
  script += `

# ── Progress bar (color by threshold) ──
PCT=\${PCT_RAW%%.*}
PCT=\${PCT:-0}
if (( PCT >= 60 )); then BAR_COLOR="$RED"
elif (( PCT >= 40 )); then BAR_COLOR="$YELLOW"
else BAR_COLOR="$GREEN"; fi

FILLED=$((PCT / 10)); EMPTY=$((10 - FILLED))
BAR=""
for ((i=0; i<FILLED; i++)); do BAR+="█"; done
for ((i=0; i<EMPTY; i++)); do BAR+="░"; done

# ── Token count (k format) ──
TOTAL_TOKENS=$((INPUT_TOKENS + CACHE_CREATE + CACHE_READ))
(( TOTAL_TOKENS == 0 && USAGE_NUMERIC > 0 )) && TOTAL_TOKENS=$USAGE_NUMERIC
USED_K=$((TOTAL_TOKENS / 1000))
TOTAL_K=$((CTX_SIZE / 1000))`;

  // ── Cache (field 2) ──
  if (has(2)) {
    script += `

# ── Cache hit rate ──
if (( TOTAL_TOKENS > 0 )); then
    CACHE_PCT=$((CACHE_READ * 100 / TOTAL_TOKENS))
    CACHE_STR="cache:\${CACHE_PCT}%"
else
    CACHE_STR="cache:--"
fi`;
  }

  // ── Git (field 3) ──
  if (has(3)) {
    script += `

# ── Git branch + dirty indicator ──
GIT_STR=""
if [[ -n "$CWD" ]] && git -C "$CWD" rev-parse --git-dir >/dev/null 2>&1; then
    BRANCH=$(git -C "$CWD" -c core.fsmonitor=false branch --show-current 2>/dev/null)
    [[ -z "$BRANCH" ]] && BRANCH="detached"
    if git -C "$CWD" -c core.fsmonitor=false diff-index --quiet HEAD -- 2>/dev/null; then
        GIT_STR="⎇ \${BRANCH}"
    else
        GIT_STR="⎇ \${BRANCH}*"
    fi
fi`;
  }

  // ── Duration (field 4) ──
  if (has(4)) {
    script += `

# ── Session duration ──
if (( DURATION_MS > 0 )); then
    SECS=$((DURATION_MS / 1000))
else
    HASH=$(echo -n "\${CWD:-default}" | md5 -q 2>/dev/null || echo -n "\${CWD:-default}" | md5sum 2>/dev/null | cut -d' ' -f1)
    START_FILE="/tmp/.claude-sl-\${HASH:0:8}"
    [[ ! -f "$START_FILE" ]] && date +%s > "$START_FILE"
    SECS=$(( $(date +%s) - $(cat "$START_FILE" 2>/dev/null || date +%s) ))
fi

if (( SECS >= 3600 )); then
    DURATION_STR="$((SECS / 3600))h$((SECS % 3600 / 60))m"
elif (( SECS >= 60 )); then
    DURATION_STR="$((SECS / 60))m"
else
    DURATION_STR="\${SECS}s"
fi`;
  }

  // ── Cost (field 5) ──
  if (has(5)) {
    script += `

# ── Cost ──
COST_STR=""
if [[ "$COST_USD" != "0" && -n "$COST_USD" ]]; then
    COST_STR=$(printf "\\$%.2f" "$COST_USD" 2>/dev/null || echo "\\$\${COST_USD}")
fi`;
  }

  // ── CWD (field 6) ──
  if (has(6)) {
    script += `

# ── CWD (abbreviate $HOME → ~) ──
DISPLAY_CWD="\${CWD/#$HOME/~}"`;
  }

  // ── Assemble Line 1 ──
  script += `

# ══════════════════════════════════════════════
# Output
# ══════════════════════════════════════════════
LINE1=""`;

  if (has(0)) {
    script += `
LINE1="\${CYAN}\${DISPLAY_MODEL}\${RST}"`;
  }

  // Progress bar
  script += `
[[ -n "$LINE1" ]] && LINE1="\${LINE1}\${SEP}"
LINE1="\${LINE1}\${BAR_COLOR}\${BAR}\${RST} \${PCT}% (\${USED_K}k/\${TOTAL_K}k)"`;

  if (has(2)) {
    script += `
LINE1="\${LINE1}\${SEP}\${CACHE_STR}"`;
  }

  if (has(3)) {
    script += `
[[ -n "$GIT_STR" ]] && LINE1="\${LINE1}\${SEP}\${MAGENTA}\${GIT_STR}\${RST}"`;
  }

  script += `

echo -e "$LINE1"`;

  // ── Assemble Line 2 (if any line-2 fields selected) ──
  if (hasLine2) {
    script += `

LINE2=""`;

    if (has(4)) {
      script += `
LINE2="⏱ \${DURATION_STR}"`;
    }

    if (has(5)) {
      script += `
if [[ -n "$COST_STR" ]]; then
    [[ -n "$LINE2" ]] && LINE2="\${LINE2}\${SEP}"
    LINE2="\${LINE2}\${COST_STR}"
fi`;
    }

    if (has(6)) {
      script += `
[[ -n "$LINE2" ]] && LINE2="\${LINE2}\${SEP}"
LINE2="\${LINE2}\${DIM}\${DISPLAY_CWD}\${RST}"`;
    }

    script += `
[[ -n "$LINE2" ]] && echo -e "$LINE2"`;
  }

  script += '\n';
  return script;
}

async function installStatusline() {
  // Check jq
  try {
    require('child_process').execSync('which jq', { stdio: 'ignore' });
  } catch {
    console.log('⚠  statusline 需要 jq，请先安装: brew install jq');
    return;
  }

  // Ask which fields
  const allFlag = process.argv.includes('--all');
  let selectedFields = STATUSLINE_FIELDS.map((_, i) => i);

  if (!allFlag) {
    console.log('\n  Statusline 显示字段：');
    console.log('  ── Line 1 ──');
    STATUSLINE_FIELDS.forEach((f, i) => {
      if (i === 4) console.log('  ── Line 2 ──');
      console.log(`    [${i + 1}] ${f.name.padEnd(20)} ${f.desc}`);
    });

    const answer = await ask('  全部显示请直接回车，或输入要显示的编号（如 1,2）：');
    if (answer) {
      const pickFields = new Set();
      for (const s of answer.split(',')) {
        const n = parseInt(s.trim(), 10);
        if (n >= 1 && n <= STATUSLINE_FIELDS.length) pickFields.add(n - 1);
      }
      selectedFields = selectedFields.filter((i) => pickFields.has(i));
      if (selectedFields.length === 0) {
        console.log('  ℹ 未选择额外字段，仅显示进度条');
      }
    }
  }

  // Ensure ~/.claude/ exists
  fs.mkdirSync(CLAUDE_DIR, { recursive: true });

  // Backup existing script
  if (fs.existsSync(STATUSLINE_PATH)) {
    const ts = Math.floor(Date.now() / 1000);
    fs.copyFileSync(STATUSLINE_PATH, `${STATUSLINE_PATH}.backup.${ts}`);
  }

  // Write script
  const script = generateStatuslineScript(selectedFields);
  fs.writeFileSync(STATUSLINE_PATH, script, { mode: 0o755 });
  console.log('  ✔ 安装 statusline 脚本 → ~/.claude/statusline-command.sh');

  // Merge settings.json
  let settings = {};
  if (fs.existsSync(SETTINGS_PATH)) {
    try {
      settings = JSON.parse(fs.readFileSync(SETTINGS_PATH, 'utf-8'));
    } catch {
      const ts = Math.floor(Date.now() / 1000);
      fs.copyFileSync(SETTINGS_PATH, `${SETTINGS_PATH}.backup.${ts}`);
    }
  }
  settings.statusLine = {
    type: 'command',
    command: `bash ${STATUSLINE_PATH}`,
  };
  fs.writeFileSync(SETTINGS_PATH, JSON.stringify(settings, null, 2) + '\n', 'utf-8');
  console.log('  ✔ 更新 settings.json → statusLine');
}

const MARKER_START = '# --- yozakura (managed) ---';
const MARKER_END = '# --- end yozakura ---';

const THEME_CONFLICT_KEYS = new Set([
  'palette', 'background', 'foreground',
  'cursor-color', 'cursor-text',
  'selection-background', 'selection-foreground',
]);

// ── Paths ──

const ghosttyDir = path.join(os.homedir(), '.config', 'ghostty');
const themesDir = path.join(ghosttyDir, 'themes');
const configPath = path.join(ghosttyDir, 'config');
const pkgRoot = path.resolve(__dirname, '..');

// ── Helpers ──

function parseKey(line) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith('#')) return null;
  const eq = trimmed.indexOf('=');
  if (eq === -1) return null;
  return trimmed.slice(0, eq).trim();
}

function ask(question) {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      rl.close();
      resolve(answer.trim());
    });
  });
}

// ── Main ──

async function main() {
  const allFlag = process.argv.includes('--all');

  console.log('\n✨ Yozakura Theme Installer\n');

  // Determine which groups to install
  let pickSet = null;

  if (!allFlag) {
    console.log('可选配置组：');
    GROUPS.forEach((g, i) => {
      console.log(`  [${i + 1}] ${g.name.padEnd(8)} ${g.desc}`);
    });
    console.log();
    const answer = await ask('全部安装请直接回车，或输入要安装的编号（如 1,3）：');
    if (answer) {
      pickSet = new Set();
      for (const s of answer.split(',')) {
        const n = parseInt(s.trim(), 10);
        if (n >= 1 && n <= GROUPS.length) pickSet.add(n - 1);
      }
    }
  }

  const selected = pickSet ? GROUPS.filter((_, i) => pickSet.has(i)) : GROUPS;
  const skipped = pickSet ? GROUPS.filter((_, i) => !pickSet.has(i)) : [];

  if (selected.length === 0) {
    console.log('未选择任何配置组，退出。');
    return;
  }

  // Ensure directories exist
  fs.mkdirSync(themesDir, { recursive: true });

  // Backup existing config
  if (fs.existsSync(configPath)) {
    const ts = Math.floor(Date.now() / 1000);
    const backupPath = `${configPath}.backup.${ts}`;
    fs.copyFileSync(configPath, backupPath);
    console.log(`✔ 备份已有配置 → config.backup.${ts}`);
  }

  // Install theme files if "主题" group is selected
  const needsThemes = selected.some((g) => g.requiresThemes);
  if (needsThemes) {
    for (const name of ['Yozakura', 'Sakura']) {
      const src = path.join(pkgRoot, 'themes', name);
      const dst = path.join(themesDir, name);
      fs.copyFileSync(src, dst);
    }
    console.log('✔ 安装主题: Yozakura + Sakura');
  }

  // Install Claude Code statusline if selected
  const needsClaudeCode = selected.some((g) => g.claudeCode);
  if (needsClaudeCode) {
    await installStatusline();
  }

  // Build selected keys and lines
  const selectedKeys = new Set();
  const newLines = [];

  for (const g of selected) {
    for (const line of g.lines) {
      const key = parseKey(line);
      if (key) selectedKeys.add(key);
      newLines.push(line);
    }
  }

  // Read existing config
  let existingLines = [];
  if (fs.existsSync(configPath)) {
    existingLines = fs.readFileSync(configPath, 'utf-8').split('\n');
  }

  // Remove old managed block and scattered selected keys
  const cleaned = [];
  let inManagedBlock = false;

  for (const line of existingLines) {
    if (line.trim() === MARKER_START) {
      inManagedBlock = true;
      continue;
    }
    if (line.trim() === MARKER_END) {
      inManagedBlock = false;
      continue;
    }
    if (inManagedBlock) continue;

    const key = parseKey(line);
    if (key && selectedKeys.has(key)) continue;

    cleaned.push(line);
  }

  // Remove trailing blank lines
  while (cleaned.length > 0 && cleaned[cleaned.length - 1].trim() === '') {
    cleaned.pop();
  }

  // Append managed block
  cleaned.push('');
  cleaned.push(MARKER_START);
  for (const line of newLines) {
    cleaned.push(line);
  }
  cleaned.push(MARKER_END);
  cleaned.push('');

  fs.writeFileSync(configPath, cleaned.join('\n'), 'utf-8');

  // Summary
  const totalLines = newLines.length;
  const skipNames = skipped.map((g) => g.name).join(', ');
  const skipMsg = skipNames ? `（跳过: ${skipNames}）` : '';
  console.log(`✔ 合并 ${selected.length} 组共 ${totalLines} 项配置${skipMsg}`);

  // Detect theme conflict keys outside managed block
  let hasConflicts = false;
  if (needsThemes) {
    const finalLines = fs.readFileSync(configPath, 'utf-8').split('\n');
    const conflicts = [];
    let inManaged = false;

    for (let i = 0; i < finalLines.length; i++) {
      const line = finalLines[i];
      if (line.trim() === MARKER_START) { inManaged = true; continue; }
      if (line.trim() === MARKER_END) { inManaged = false; continue; }
      if (inManaged) continue;

      const key = parseKey(line);
      if (key && THEME_CONFLICT_KEYS.has(key)) {
        conflicts.push({ lineNum: i + 1, text: line.trim() });
      }
    }

    if (conflicts.length > 0) {
      console.log('\n⚠  以下配置会覆盖主题配色：');
      for (const c of conflicts) {
        console.log(`     第 ${c.lineNum} 行: ${c.text}`);
      }

      const doFix = allFlag || (await ask('   是否自动注释掉这些行？(Y/n) '));
      if (doFix === true || !doFix || doFix.toLowerCase() === 'y') {
        const conflictLineNums = new Set(conflicts.map((c) => c.lineNum));
        const patched = finalLines.map((line, i) =>
          conflictLineNums.has(i + 1) ? `# ${line}` : line
        );
        fs.writeFileSync(configPath, patched.join('\n'), 'utf-8');
        console.log('   ✔ 已注释掉冲突配置');
      } else {
        hasConflicts = true;
        console.log(`   配置文件位置: ${configPath}`);
        console.log('   请手动移除或注释掉（加 # 前缀），然后按 Cmd+Shift+, 重载配置。');
      }
    }
  }

  if (hasConflicts) {
    console.log('');
  } else {
    console.log('\n✅ Done! 按 Cmd+Shift+, 重载 Ghostty 即可生效\n');
  }
}

main().catch((err) => {
  console.error('安装失败:', err.message);
  process.exit(1);
});
